// for loop

for (let i = 0; i < 5; i++) {
    console.log(i)
}

const names = ['Kevin', 'Sambhav', 'Dev']
for (let i = 0; i < names.length; i++) {
    console.log(names[i])
}

// while loop
let i = 0
while (i < 5) {
    console.log('while loop: ', i)
    i += 1
}

// do while loop
i = 5
do {
    console.log('do while loop: ', i)
    i += 1
} while (i < 5)